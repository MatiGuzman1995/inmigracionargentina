// === FUNCIÓN PARA DETECTAR PÁGINA ===
function obtenerPagina() {
  const nombre = location.pathname.split("/").pop();
  if (!nombre || nombre === "" || nombre === "index.html") return "index";
  return nombre.replace(".html", "");
}

// === FUNCIÓN PRINCIPAL ===
function cambiarIdioma(lang) {
  // Guardar idioma seleccionado
  localStorage.setItem("idioma", lang);

  // Detectar página actual
  const pagina = obtenerPagina();

  // --- Traducir contenido específico de la página ---
  const textos = traducciones[lang]?.[pagina];
  if (textos) {
    for (const id in textos) {
      const el = document.getElementById(id);
      if (el) el.innerHTML = textos[id];
    }
  }

  // --- Traducir menú global ---
  if (typeof traduccionesMenu !== "undefined" && traduccionesMenu[lang]) {
    for (const id in traduccionesMenu[lang]) {
      const el = document.getElementById(id);
      if (el) el.textContent = traduccionesMenu[lang][id];
    }
  }
}

// === CARGAR IDIOMA GUARDADO ===
window.addEventListener("DOMContentLoaded", () => {
  const lang = localStorage.getItem("idioma") || "en";
  cambiarIdioma(lang);
});
